let nota = 90;

switch (nota) {
  case nota >= 90:
    console.log("Nota = A");
    break;
  case nota >= 80:
    console.log("Nota = b");
    break;
  case nota >= 70:
    console.log("Nota = c");
    break;
  case nota >= 60:
    console.log("Nota = d");
    break;
  case nota >= 50:
    console.log("Nota = e");
    break;
  case nota > 50:
    console.log("Nota = f");
    break;

  default:
    break;
}
